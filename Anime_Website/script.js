let currentIndex = 0;
const main = document.querySelector('main');
let prevButton = document.getElementById('prevBtn');
let nextButton = document.getElementById('nextBtn');

function initSlider() {
    const currentSlider = sliders[currentIndex];
    main.style.backgroundImage = `url(${currentSlider.imageAnime})`
    main.style.backgroundRepeat = "no-repeat"
    main.style.backgroundSize = "cover"
    main.style.backgroundPosition = "center"
}
console.log(sliders[currentIndex])
function prevSlide() 
{
    currentIndex = (currentIndex === 0) ? sliders.length - 1 : currentIndex - 1;
    initSlider();
    
}

function nextSlide() 
{
        
    currentIndex = (currentIndex === sliders.length - 1) ? 0 : currentIndex + 1;
      
    initSlider(); 
    
}


prevButton.addEventListener("click", prevSlide)
nextButton.addEventListener("click", nextSlide)

setInterval(nextSlide, 5000);

initSlider()
